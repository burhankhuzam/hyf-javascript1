function vehicleType(color, object) {
    if (color == "Blue" && object == 1){
        console.log("a  " + color + " car")
    } else if (color == "Red" && object == 2) {
        console.log("a  " + color + " motorbike")
    } else {
        console.log("The object has no style")
    }
}
vehicleType("Blue", 1)
vehicleType("Red", 2)


